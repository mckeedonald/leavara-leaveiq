// POST /api/ada/cases/:caseId/physician-cert  (generate + send)
router.post("/ada/cases/:caseId/physician-cert", requireAuth, async (req: Request, res: Response) => {
  const authed = req as AuthenticatedRequest;
  const { caseId } = req.params;
  // Accept certContent from frontend (pre-generated preview) to avoid a redundant AI call.
  // employeeEmail is the recipient — frontend sends it as "employeeEmail".
  const { employeeEmail, certContent: providedContent } = req.body as {
    employeeEmail?: string;
    certContent?: string;
  };

  try {
    const [adaCase] = await db.select().from(adaCasesTable)
      .where(and(eq(adaCasesTable.id, caseId), eq(adaCasesTable.organizationId, authed.user.organizationId)))
      .limit(1);
    if (!adaCase) { res.status(404).json({ error: "Case not found" }); return; }

    // Re-use the letter the user already previewed; only regenerate if not provided
    const certContent = providedContent?.trim() || await generatePhysicianCertRequest(adaCase);

    // Build portal return instructions (appended to cert before PDF generation)
    const appUrl = getAppUrl();
    const portalUrl = adaCase.accessToken
      ? `${appUrl}/leaveiq/portal?token=${adaCase.accessToken}`
      : null;
    const returnInstructions = [
      "",
      "─────────────────────────────────────────────────────────────────────",
      "HOW TO RETURN THIS COMPLETED FORM",
      "─────────────────────────────────────────────────────────────────────",
      "",
      "Once completed, please return this form using ONE of the following methods:",
      "",
      portalUrl
        ? `1. EMPLOYEE PORTAL (preferred): The employee can upload this completed form at:\n   ${portalUrl}\n   After visiting the portal, select "Upload Document" and choose "Medical Certification" as the document type.`
        : "1. EMPLOYEE PORTAL: Ask the employee for their portal link to upload this completed form directly.",
      "",
      `2. EMAIL: Return to HR at the contact information provided on this letter.`,
      "",
      `3. FAX/MAIL: Contact HR for mailing or fax details.`,
      "",
      `Case Reference: ${adaCase.caseNumber}`,
      `Please return within 15 business days of the date of this request.`,
    ].join("\n");

    const certWithInstructions = certContent + returnInstructions;

    // Generate PDF using pure Node.js (same approach as FMLA med cert)
    const { buildAdaCertPdf } = await import("../lib/adaPdfBuilder.js").catch(() => ({ buildAdaCertPdf: null }));
    let pdfBuffer: Buffer | null = null;
    if (buildAdaCertPdf) {
      try { pdfBuffer = buildAdaCertPdf(certWithInstructions); } catch { /* fall through to text-only */ }
    }
    // Fallback: build PDF inline using the same algorithm as buildMedCertPdf
    if (!pdfBuffer) {
      pdfBuffer = buildSimplePdf(certWithInstructions);
    }
    const pdfBase64 = pdfBuffer.toString("base64");

    // Send email — use the explicit recipient address, fall back to case email
    const recipientEmail = employeeEmail?.trim() || adaCase.employeeEmail;
    let emailSent = false;
    let emailError: string | undefined;
    if (recipientEmail) {
      try {
        emailSent = await sendNoticeEmail({
          to: recipientEmail,
          content: `Please find the ADA Physician Certification Request attached as a PDF.\n\nCase Number: ${adaCase.caseNumber}\n\nPlease complete the attached form and return it within 15 business days.`,
          caseNumber: adaCase.caseNumber,
          employeeNumber: adaCase.employeeNumber ?? "",
          noticeType: "ADA_PHYSICIAN_CERT",
          attachments: [
            {
              filename: `ADA_Physician_Cert_${adaCase.caseNumber ?? "request"}.pdf`,
              content: pdfBase64,
            },
          ],
        });
        if (!emailSent) {
          emailError = "Email delivery is not configured (RESEND_API_KEY missing). The certification has been recorded.";
        }
      } catch (emailErr) {
        // Email delivery failure is logged but non-fatal — cert is still recorded in the case
        logger.error({ emailErr }, "ADA physician cert email delivery failed — recording cert anyway");
        emailError = emailErr instanceof Error ? emailErr.message : "Email delivery failed";
      }
    } else {
      emailError = "No email address on file for this employee. The certification has been recorded.";
    }

    // Mark cert as sent; use "awaiting_physician" so the UI status badge renders correctly
    await db.update(adaCasesTable).set({
      physicianCertSentAt: new Date(),
      status: "awaiting_physician",
      displayStatus: "Awaiting Physician Cert",
      updatedAt: new Date(),
    }).where(eq(adaCasesTable.id, caseId));

    // Log it
    await db.insert(adaInteractiveLogTable).values({
      caseId,
      entryType: "physician_cert_sent",
      authorId: authed.user.sub,
      authorName: [authed.user.firstName, authed.user.lastName].filter(Boolean).join(" ") || authed.user.email,
      authorRole: "hr",
      content: `ADA Physician Certification form sent to ${recipientEmail ?? "employee"} on ${new Date().toLocaleDateString()}.`,
      metadata: JSON.stringify({ certContent }),
    });

    res.json({ ok: true, certContent, emailSent, emailError });
  } catch (err) {
    logger.error({ err }, "ADA physician cert error");
    res.status(500).json({ error: "Failed to send physician certification" });
  }
});

