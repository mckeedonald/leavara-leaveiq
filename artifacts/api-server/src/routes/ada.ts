/** Pure Node.js PDF generator for ADA documents (same algorithm as buildMedCertPdf in cases.ts) */
function buildSimplePdf(content: string): Buffer {
  const PW = 612, PH = 792, MX = 50, MY = 50;
  const CW = PW - MX * 2;
  function esc(s: string) { return s.replace(/\\/g, "\\\\").replace(/\(/g, "\\(").replace(/\)/g, "\\)"); }
  function approxW(t: string, sz: number) { return t.length * sz * 0.52; }
  function wordWrap(t: string, sz: number, maxW: number): string[] {
    if (approxW(t, sz) <= maxW) return [t];
    const words = t.split(" "); const lines: string[] = []; let cur = "";
    for (const w of words) { const nx = cur ? cur + " " + w : w; if (approxW(nx, sz) <= maxW) { cur = nx; } else { if (cur) lines.push(cur); cur = w; } }
    if (cur) lines.push(cur); return lines.length ? lines : [t];
  }
  interface Row { text: string; bold: boolean; sz: number; x: number; lineH: number; }
  const rows: Row[] = [];
  function addRow(text: string, bold: boolean, sz: number, indent = 0, extra = 0) { rows.push({ text, bold, sz, x: MX + indent, lineH: sz + 4 + extra }); }
  addRow("ADA PHYSICIAN CERTIFICATION REQUEST", true, 13, 0, 6);
  addRow("Generated by Leavara LeaveIQ  -  For HR Use Only", false, 9, 0, 8);
  for (const raw of content.split("\n")) {
    const trimmed = raw.trim();
    if (!trimmed) { addRow("", false, 10, 0, 2); continue; }
    const isHdr = /^[A-Z\-─]{4,}/.test(trimmed) || /^\d+\.\s+[A-Z]/.test(trimmed);
    const isIndented = raw.startsWith("  ") || raw.startsWith("\t");
    if (isHdr) { addRow("", false, 3, 0, 0); for (const wl of wordWrap(trimmed, 10, CW)) addRow(wl, true, 10, 0, 0); }
    else { for (const wl of wordWrap(trimmed, 10, CW - (isIndented ? 20 : 0))) addRow(wl, false, 10, isIndented ? 20 : 0, 0); }
  }
  const startY = PH - MY - 18; const pages: { row: Row; y: number }[][] = []; let curPage: { row: Row; y: number }[] = []; let y = startY;
  for (const row of rows) { if (y - row.sz < MY + 20 && curPage.length > 0) { pages.push(curPage); curPage = []; y = startY; } if (row.text) curPage.push({ row, y }); y -= row.lineH; }
  if (curPage.length > 0 || pages.length === 0) pages.push(curPage);
  const streams = pages.map(pg => { const cmds = ["BT"]; let cf = "", cs = 0; for (const { row, y: ry } of pg) { const fn = row.bold ? "/F2" : "/F1"; if (fn !== cf || row.sz !== cs) { cmds.push(`${fn} ${row.sz} Tf`); cf = fn; cs = row.sz; } cmds.push(`1 0 0 1 ${row.x} ${ry.toFixed(1)} Tm`); cmds.push(`(${esc(row.text)}) Tj`); } cmds.push("ET"); return cmds.join("\n"); });
  const nP = pages.length, fpO = 3, fnr = fpO + nP, fbd = fnr + 1, fst = fbd + 1, tot = fst + nP;
  const parts: string[] = []; const off: number[] = new Array(tot).fill(0); let pos = 0;
  function w(s: string) { parts.push(s); pos += Buffer.byteLength(s, "latin1"); }
  w("%PDF-1.4\n%\xe2\xe3\xcf\xd3\n"); off[1] = pos; w(`1 0 obj\n<< /Type /Catalog /Pages 2 0 R >>\nendobj\n`);
  const kids = Array.from({ length: nP }, (_, i) => `${fpO + i} 0 R`).join(" "); off[2] = pos; w(`2 0 obj\n<< /Type /Pages /Kids [${kids}] /Count ${nP} >>\nendobj\n`);
  for (let i = 0; i < nP; i++) { off[fpO + i] = pos; w(`${fpO + i} 0 obj\n<< /Type /Page /Parent 2 0 R /MediaBox [0 0 ${PW} ${PH}] /Contents ${fst + i} 0 R /Resources << /Font << /F1 ${fnr} 0 R /F2 ${fbd} 0 R >> >> >>\nendobj\n`); }
  off[fnr] = pos; w(`${fnr} 0 obj\n<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica /Encoding /WinAnsiEncoding >>\nendobj\n`);
  off[fbd] = pos; w(`${fbd} 0 obj\n<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold /Encoding /WinAnsiEncoding >>\nendobj\n`);
  for (let i = 0; i < nP; i++) { const s = streams[i]; off[fst + i] = pos; w(`${fst + i} 0 obj\n<< /Length ${Buffer.byteLength(s, "latin1")} >>\nstream\n${s}\nendstream\nendobj\n`); }
  const xp = pos; w(`xref\n0 ${tot}\n`); w(`0000000000 65535 f \n`);
  for (let i = 1; i < tot; i++) w(`${String(off[i]).padStart(10, "0")} 00000 n \n`);
  w(`trailer\n<< /Size ${tot} /Root 1 0 R >>\nstartxref\n${xp}\n%%EOF\n`);
  return Buffer.from(parts.join(""), "latin1");
}

function generateCaseNumber(): string {
  const now = new Date();
  const year = now.getFullYear();
  const month = String(now.getMonth() + 1).padStart(2, "0");
  const rand = Math.floor(Math.random() * 9000) + 1000;
  return `ADA-${year}${month}-${rand}`;
}

