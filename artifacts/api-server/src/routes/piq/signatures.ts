// GET /api/piq/sign?token=xxx  — public, no auth
router.get("/piq/sign", async (req: Request, res: Response): Promise<void> => {
  const token = req.query["token"] as string | undefined;
  if (!token) { res.status(400).json({ error: "token required" }); return; }

  try {
    const [sig] = await db
      .select()
      .from(piqSignaturesTable)
      .where(eq(piqSignaturesTable.employeeAccessToken, token))
      .limit(1);

    if (!sig) { res.status(404).json({ error: "Invalid or expired link" }); return; }
    if (sig.status === "declined") { res.json({ status: "declined", employeeComment: sig.employeeComment }); return; }
    if (sig.status === "completed" || sig.status === "employee_signed" || sig.status === "manager_signed") {
      res.json({ status: sig.status, alreadySigned: true }); return;
    }

    // Load case and document
    const [caseRow] = await db.select().from(piqCasesTable).where(eq(piqCasesTable.id, sig.caseId)).limit(1);
    const [employee] = await db.select().from(employeesTable).where(eq(employeesTable.id, sig.employeeId)).limit(1);
    const [doc] = await db
      .select()
      .from(piqDocumentsTable)
      .where(eq(piqDocumentsTable.caseId, sig.caseId))
      .orderBy(piqDocumentsTable.version)
      .limit(1);

    const [org] = await db
      .select({ name: organizationsTable.name })
      .from(organizationsTable)
      .where(eq(organizationsTable.id, sig.organizationId))
      .limit(1);

    const docLabel = (doc as any)?.documentTypeLabel ?? "Performance Document";
    const content = (doc as any)?.content;
    const docText = content ? documentContentToText(content, docLabel) : "";

    // Mark as viewed
    if (sig.status === "sent") {
      await db.update(piqSignaturesTable).set({ status: "viewed", updatedAt: new Date() }).where(eq(piqSignaturesTable.id, sig.id));
    }

    res.json({
      status: "ok",
      caseNumber: caseRow?.caseNumber,
      employeeName: employee?.fullName,
      organizationName: org?.name,
      docLabel,
      docText,
      signatureId: sig.id,
    });
  } catch (err) {
    logger.error({ err }, "get sign page error");
    res.status(500).json({ error: "Internal server error" });
  }
});

// POST /api/piq/sign  — public, no auth
router.post("/piq/sign", async (req: Request, res: Response): Promise<void> => {
  const { token, action, signatureData, comment } = req.body as {
    token?: string;
    action?: "sign" | "decline";
    signatureData?: string;
    comment?: string;
  };

  if (!token || !action) { res.status(400).json({ error: "token and action required" }); return; }

  try {
    const [sig] = await db
      .select()
      .from(piqSignaturesTable)
      .where(eq(piqSignaturesTable.employeeAccessToken, token))
      .limit(1);

    if (!sig) { res.status(404).json({ error: "Invalid or expired link" }); return; }
    if (sig.status !== "sent" && sig.status !== "viewed") {
      res.status(409).json({ error: "Document has already been actioned" }); return;
    }

    if (action === "decline") {
      await db.update(piqSignaturesTable).set({
        status: "declined",
        employeeComment: comment?.trim() || null,
        updatedAt: new Date(),
      }).where(eq(piqSignaturesTable.id, sig.id));

      // Update case status back to manager_revision
      await db.update(piqCasesTable).set({ status: "manager_revision", updatedAt: new Date() })
        .where(eq(piqCasesTable.id, sig.caseId));

      res.json({ ok: true, status: "declined" });
      return;
    }

    if (action === "sign") {
      if (!signatureData?.trim()) { res.status(400).json({ error: "signatureData required" }); return; }

      await db.update(piqSignaturesTable).set({
        status: "employee_signed",
        employeeSignatureData: signatureData.trim(),
        employeeSignedAt: new Date(),
        updatedAt: new Date(),
      }).where(eq(piqSignaturesTable.id, sig.id));

      res.json({ ok: true, status: "employee_signed" });
      return;
    }

    res.status(400).json({ error: "action must be sign or decline" });
  } catch (err) {
    logger.error({ err }, "post sign error");
    res.status(500).json({ error: "Internal server error" });
  }
});

// POST /api/performiq/cases/:caseId/signatures/manager-sign
router.post(
  "/performiq/cases/:caseId/signatures/manager-sign",
  requirePiqAuth,
  async (req: Request, res: Response): Promise<void> => {
    const authed = req as PiqAuthenticatedRequest;
    const { caseId } = req.params;
    const { signatureData } = req.body as { signatureData?: string };

    if (!signatureData?.trim()) { res.status(400).json({ error: "signatureData required" }); return; }

    try {
      const [sig] = await db
        .select()
        .from(piqSignaturesTable)
        .where(and(
          eq(piqSignaturesTable.caseId, caseId),
          eq(piqSignaturesTable.provider, "native"),
          eq(piqSignaturesTable.organizationId, authed.piqUser.organizationId!),
        ))
        .limit(1);

      if (!sig) { res.status(404).json({ error: "Signature record not found" }); return; }
      if (sig.status !== "employee_signed") {
        res.status(409).json({ error: `Employee must sign first (current status: ${sig.status})` }); return;
      }

      // Load case and document for PDF generation
      const [caseRow] = await db.select().from(piqCasesTable).where(eq(piqCasesTable.id, caseId)).limit(1);
      const [employee] = await db.select().from(employeesTable).where(eq(employeesTable.id, sig.employeeId)).limit(1);
      const [doc] = await db
        .select()
        .from(piqDocumentsTable)
        .where(eq(piqDocumentsTable.caseId, caseId))
        .orderBy(desc(piqDocumentsTable.version))
        .limit(1);

      const docLabel = (doc as any)?.documentTypeLabel ?? "Performance Document";
      const content = (doc as any)?.content;
      const docText = content ? documentContentToText(content, docLabel) : "";
      const managerSignedAt = new Date();

      // Generate final signed PDF
      const pdfBuffer = buildDocumentPdf(
        docText,
        docLabel,
        employee?.fullName ?? "Employee",
        sig.employeeSignatureData,
        sig.employeeSignedAt ? new Date(sig.employeeSignedAt) : null,
        signatureData.trim(),
        managerSignedAt,
      );
      const pdfBase64 = pdfBuffer.toString("base64");

      // Update signature record
      await db.update(piqSignaturesTable).set({
        status: "completed",
        managerSignatureData: signatureData.trim(),
        managerSignedAt,
        signedAt: managerSignedAt,
        signedPdfContent: pdfBase64,
        updatedAt: new Date(),
      }).where(eq(piqSignaturesTable.id, sig.id));

      // Close the case
      await db.update(piqCasesTable).set({
        status: "closed",
        updatedAt: new Date(),
      }).where(eq(piqCasesTable.id, caseId));

      logger.info({ caseId, caseNumber: caseRow?.caseNumber }, "PerformIQ case signed and closed");

      res.json({ ok: true, status: "completed" });
    } catch (err) {
      logger.error({ err, caseId }, "manager sign error");
      res.status(500).json({ error: "Internal server error" });
    }
  },
);

// GET /api/performiq/cases/:caseId/signatures/download-pdf
router.get(
  "/performiq/cases/:caseId/signatures/download-pdf",
  requirePiqAuth,
  async (req: Request, res: Response): Promise<void> => {
    const authed = req as PiqAuthenticatedRequest;
    const { caseId } = req.params;

    try {
      const [sig] = await db
        .select()
        .from(piqSignaturesTable)
        .where(and(
          eq(piqSignaturesTable.caseId, caseId),
          eq(piqSignaturesTable.provider, "native"),
          eq(piqSignaturesTable.organizationId, authed.piqUser.organizationId!),
        ))
        .limit(1);

      if (!sig || !sig.signedPdfContent) {
        res.status(404).json({ error: "Signed PDF not available yet" }); return;
      }

      const [caseRow] = await db.select({ caseNumber: piqCasesTable.caseNumber }).from(piqCasesTable).where(eq(piqCasesTable.id, caseId)).limit(1);
      const pdfBuffer = Buffer.from(sig.signedPdfContent, "base64");
      const filename = `${caseRow?.caseNumber ?? caseId}_signed.pdf`;

      res.setHeader("Content-Type", "application/pdf");
      res.setHeader("Content-Disposition", `attachment; filename="${filename}"`);
      res.send(pdfBuffer);
    } catch (err) {
      logger.error({ err }, "download signed pdf error");
      res.status(500).json({ error: "Internal server error" });
    }
  },
);

export default router;
