// POST /api/performiq/cases/:caseId/signatures/request
router.post(
  "/performiq/cases/:caseId/signatures/request",
  requirePiqAuth,
  async (req: Request, res: Response): Promise<void> => {
    const authed = req as PiqAuthenticatedRequest;
    const { caseId } = req.params;

    try {
      // Load case + employee + document
      const [caseRow] = await db
        .select()
        .from(piqCasesTable)
        .where(and(eq(piqCasesTable.id, caseId), eq(piqCasesTable.organizationId, authed.piqUser.organizationId!)))
        .limit(1);
      if (!caseRow) { res.status(404).json({ error: "Case not found" }); return; }

      const [employee] = await db.select().from(employeesTable).where(eq(employeesTable.id, caseRow.employeeId)).limit(1);
      if (!employee) { res.status(404).json({ error: "Employee not found" }); return; }

      const employeeEmail = employee.workEmail ?? employee.personalEmail;
      if (!employeeEmail) { res.status(400).json({ error: "Employee has no email on file" }); return; }

      // Load latest document
      const [doc] = await db
        .select()
        .from(piqDocumentsTable)
        .where(eq(piqDocumentsTable.caseId, caseId))
        .orderBy(desc(piqDocumentsTable.version))
        .limit(1);

      // Load document type label
      const [docType] = caseRow.documentTypeId
        ? await db.select({ displayLabel: piqDocumentTypesTable.displayLabel }).from(piqDocumentTypesTable).where(eq(piqDocumentTypesTable.id, caseRow.documentTypeId)).limit(1)
        : [null];
      const docLabel = docType?.displayLabel ?? "Performance Document";

      // Generate employee access token
      const employeeAccessToken = crypto.randomBytes(32).toString("hex");
      const signUrl = `${getAppUrl()}/performiq/sign?token=${employeeAccessToken}`;

      // Create (or replace) signature record
      await db.delete(piqSignaturesTable).where(
        and(eq(piqSignaturesTable.caseId, caseId), eq(piqSignaturesTable.provider, "native")),
      );

      const [sig] = await db.insert(piqSignaturesTable).values({
        caseId,
        organizationId: authed.piqUser.organizationId!,
        employeeId: employee.id,
        method: "esignature",
        provider: "native",
        status: "sent",
        deliveredBy: authed.piqUser.sub,
        deliveryDate: new Date(),
        employeeAccessToken,
      }).returning();
      try {
        await sendNoticeEmail({
          to: employeeEmail,
          subject: `Action Required: Review and Sign — ${docLabel}`,
          noticeType: "PIQ_ESIGNATURE_REQUEST",
          content: `Dear ${employee.fullName},

A ${docLabel} has been prepared for your review and signature.

Please review the document and provide your electronic signature by clicking the link below:

${signUrl}

This link is unique to you. Please do not share it.

If you have questions about this document, please contact HR.

Case: ${caseRow.caseNumber}
Document: ${docLabel}`,
        });
      } catch (emailErr) {
        logger.warn({ emailErr, caseId }, "Failed to send e-signature email");
      }

      res.json({ ok: true, signatureId: sig.id, employeeEmail });
    } catch (err) {
      logger.error({ err, caseId }, "signature request error");
      res.status(500).json({ error: "Internal server error" });
    }
  },
);

